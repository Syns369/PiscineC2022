#include <stdlib.h>

char	*ft_input(char *str)
{
	int	i;
	int	j;
	char	tab[16];

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] != ' ')
		{
			tab[j] = str[i];
			j++;
		}
	i++;
	}
	str = tab;
	return (str);
}
